public class Ej01CuentaBancaria {
    private int numeroCuenta;
    private long dniCliente;
    private double  saldoActual=0;

    public Ej01CuentaBancaria() {
    }

    public Ej01CuentaBancaria(int numeroCuenta, int dniCliente, int saldoActual) {
        this.numeroCuenta = numeroCuenta;
        this.dniCliente = dniCliente;
        this.saldoActual = saldoActual;
        
    }

    public int getNumeroCuenta() {
        return numeroCuenta;
    }

    public void setNumeroCuenta(int numeroCuenta) {
        this.numeroCuenta = numeroCuenta;
    }

    public long getDniCliente() {
        return dniCliente;
    }

    public void setDniCliente(int dniCliente) {
        this.dniCliente = dniCliente;
    }

    public double getSaldoActual() {
        return saldoActual;
    }

    public double setSaldoActual() {
      return  this.saldoActual = saldoActual;
    }
    
}
